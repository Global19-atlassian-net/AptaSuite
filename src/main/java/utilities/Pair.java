package utilities;

/**
 *	@author Jan Hoinka
 *	Implements the concept of a generic pair
 *
 */
public class Pair<V,K> {
	
	public final V first;
	public final K second;

	public Pair(V first, K second) {
		
		this.first = first;
		this.second = second;
		
	}

	
}
